package commons.entities.game;

/**
 * The power-up types that can be played.
 */
public enum PowerUp {
    /**
     * The halve time power-up.
     */
    HalveTime,
    /**
     * The point doubling power-up.
     */
    DoublePoints,
    /**
     * The power-up to eliminate an incorrect answer
     */
    IncorrectAnswer
}